#[derive(LayoutMap)]
#[layout_map(
    structure(
        byte_order = "little",
        field(name = "signature", data_type = "ByteString<4>"),
        field(name = "checksum", data_type = "u32", format = "hex"),
        field(name = "sequence_number", data_type = "u64"),
        field(name = "file_write_identifier", data_type = "Uuid"),
        field(name = "data_write_identifier", data_type = "Uuid"),
        field(name = "log_identifier", data_type = "Uuid"),
        field(name = "log_format_version", data_type = "u16"),
        field(name = "format_version", data_type = "u16"),
        field(name = "log_size", data_type = "u32"),
        field(name = "log_offset", data_type = "u64", format = "hex"),
        field(name = "unknown1", data_type = "[u8; 4016]"),
    ),
    method(name = "debug_read_data"),
    method(name = "read_at_position")
)]
/// Virtual Hard Disk version 2 (VHDX) image header.
pub struct VhdxImageHeader {
    /// Sequence number.
    pub sequence_number: u64,

    /// Data write identifier.
    pub data_write_identifier: Uuid,

    /// Format version.
    pub format_version: u16,
}

impl VhdxImageHeader {
    /// Creates a new image header.
    pub fn new() -> Self {
        Self {
            sequence_number: 0,
            data_write_identifier: Uuid::new(),
            format_version: 0,
        }
    }

    /// Reads the image header from a buffer.
    pub fn read_data(&mut self, data: &[u8]) -> io::Result<()> {
        if data.len() != 4096 {
            return Err(io::Error::new(
                io::ErrorKind::InvalidInput,
                format!("Unsupported data size"),
            ));
        }
        if data[0..4] != VHDX_IMAGE_HEADER_SIGNATURE {
            return Err(io::Error::new(
                io::ErrorKind::InvalidData,
                format!("Unsupported signature"),
            ));
        }
        let stored_checksum: u32 = bytes_to_u32_le!(data, 4);

        self.sequence_number = bytes_to_u64_le!(data, 8);
        self.data_write_identifier = Uuid::from_le_bytes(&data[32..48]);
        self.format_version = bytes_to_u16_le!(data, 66);

        let mut crc32_context: ReversedCrc32Context = ReversedCrc32Context::new(0x82f63b78, 0);

        crc32_context.update(&data[0..4]);

        let empty_data: [u8; 4] = [0; 4];
        crc32_context.update(&empty_data);

        crc32_context.update(&data[8..4096]);

        let calculated_checksum: u32 = crc32_context.finalize();

        if stored_checksum != 0 && stored_checksum != calculated_checksum {
            return Err(io::Error::new(
                io::ErrorKind::InvalidData,
                format!(
                    "Mismatch between stored: 0x{:08x} and calculated: 0x{:08x} checksums",
                    stored_checksum, calculated_checksum
                ),
            ));
        }
        Ok(())
    }
}

