use std::io::SeekFrom;
use std::path::PathBuf;

use keramics_core::{DataStream, DataStreamReference, ErrorTrace, open_os_data_stream};
use keramics_formats::gpt::{GptPartition, GptVolumeSystem};

fn open_volume_system(path: &PathBuf) -> Result<GptVolumeSystem, ErrorTrace> {
    let data_stream: DataStreamReference = match open_os_data_stream(path) {
        Ok(data_stream) => data_stream,
        Err(error) => {
            return Err(keramics_core::error_trace_new_with_error!(
                "Unable to open data stream",
                error
            ));
        }
    };
    let mut volume_system: GptVolumeSystem = GptVolumeSystem::new();

    match volume_system.read_data_stream(&data_stream) {
        Ok(_) => {}
        Err(mut error) => {
            keramics_core::error_trace_add_frame!(
                error,
                "Unable to read GPT volume system from data stream"
            );
            return Err(error);
        }
    }
    Ok(volume_system)
}

