use std::io::SeekFrom;

use keramics_core::mediator::{Mediator, MediatorReference};
use keramics_core::{DataStreamReference, ErrorTrace};
use keramics_types::{bytes_to_u16_le, bytes_to_u32_le};

use super::enums::FatFormat;

/// File Allocation Table (FAT) cluster block allocation table.
pub struct FatBlockAllocationTable {
    /// Mediator.
    mediator: MediatorReference,

    /// Format.
    pub format: FatFormat,

    /// Offset.
    offset: u64,

    /// Number of entries.
    number_of_entries: u32,

    /// First cluster offset.
    pub first_cluster_offset: u64,

    /// Cluster block size.
    pub cluster_block_size: u32,
}

impl FatBlockAllocationTable {
    /// Creates a new block allocation table.
    pub fn new(
        format: &FatFormat,
        offset: u64,
        number_of_entries: u32,
        first_cluster_offset: u64,
        cluster_block_size: u32,
    ) -> Self {
        Self {
            mediator: Mediator::current(),
            format: format.clone(),
            offset: offset,
            number_of_entries: number_of_entries,
            first_cluster_offset: first_cluster_offset,
            cluster_block_size: cluster_block_size,
        }
    }

    /// Retrieves the largest cluster block number.
    pub fn get_largest_cluster_block_number(&self) -> u32 {
        match &self.format {
            FatFormat::Fat12 => 0x00000ff0,
            FatFormat::Fat16 => 0x0000fff0,
            FatFormat::Fat32 => 0x0ffffff0,
        }
    }

    /// Reads a block allocation table entry.
    pub fn read_entry(
        &self,
        data_stream: &DataStreamReference,
        entry_index: u32,
    ) -> Result<u32, ErrorTrace> {
        if entry_index >= self.number_of_entries {
            return Err(keramics_core::error_trace_new!(format!(
                "Unsupported entry index: {} value out of bounds",
                entry_index
            )));
        }
        let entry_offset: u64 = match &self.format {
            FatFormat::Fat12 => ((entry_index as u64) / 2) * 3,
            FatFormat::Fat16 => (entry_index as u64) * 2,
            FatFormat::Fat32 => (entry_index as u64) * 4,
        };
        let entry_size: usize = match &self.format {
            FatFormat::Fat12 => 3,
            FatFormat::Fat16 => 2,
            FatFormat::Fat32 => 4,
        };
        let mut data: Vec<u8> = vec![0; entry_size];

        keramics_core::data_stream_read_exact_at_position!(
            data_stream,
            &mut data,
            SeekFrom::Start(self.offset + entry_offset)
        );
        let entry: u32 = match &self.format {
            FatFormat::Fat12 => {
                if entry_index % 2 == 0 {
                    (bytes_to_u16_le!(data, 0) & 0x0fff) as u32
                } else {
                    (bytes_to_u16_le!(data, 1) >> 4) as u32
                }
            }
            FatFormat::Fat16 => bytes_to_u16_le!(data, 0) as u32,
            FatFormat::Fat32 => {
                bytes_to_u32_le!(data, 0)
            }
        };
        if self.mediator.debug_output {
            self.mediator
                .debug_print(format!("FatBlockAllocationTableEntry: {{\n"));
            self.mediator
                .debug_print(format!("    entry_index: {},\n", entry_index));
            self.mediator.debug_print(format!(
                "    entry_offset: {} (0x{:x}),\n",
                self.offset + entry_offset,
                self.offset + entry_offset
            ));
            self.mediator
                .debug_print(format!("    cluster_block_number: {},\n", entry));
            self.mediator.debug_print(format!("}}\n"));
        }
        Ok(entry)
    }
}

