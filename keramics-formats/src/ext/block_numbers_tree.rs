use std::cmp::min;
use std::io;

use keramics_core::DataStreamReference;
use keramics_core::mediator::{Mediator, MediatorReference};
use keramics_types::bytes_to_u32_le;

use super::block_range::{ExtBlockRange, ExtBlockRangeType};

/// Extended File System (ext) block numbers tree.
pub struct ExtBlockNumbersTree {
    /// Mediator.
    mediator: MediatorReference,

    /// Block size.
    block_size: u32,

    /// Number of blocks per block.
    number_of_blocks_per_block: u32,

    /// Number of blocks.
    number_of_blocks: u64,
}

impl ExtBlockNumbersTree {
    /// Creates new block numbers tree.
    pub fn new(block_size: u32, number_of_blocks: u64) -> Self {
        Self {
            mediator: Mediator::current(),
            block_size: block_size,
            number_of_blocks_per_block: block_size / 4,
            number_of_blocks: number_of_blocks,
        }
    }

    /// Reads the block numbers tree from an inode data reference.
    pub fn read_data_reference(
        &mut self,
        data_reference: &[u8],
        data_stream: &DataStreamReference,
        block_ranges: &mut Vec<ExtBlockRange>,
    ) -> io::Result<()> {
        let mut logical_block_number: u64 = 0;

        self.read_node_data(
            &data_reference[0..48],
            data_stream,
            &mut logical_block_number,
            block_ranges,
            0,
        )?;
        self.read_node_data(
            &data_reference[48..52],
            data_stream,
            &mut logical_block_number,
            block_ranges,
            1,
        )?;
        self.read_node_data(
            &data_reference[52..56],
            data_stream,
            &mut logical_block_number,
            block_ranges,
            2,
        )?;
        self.read_node_data(
            &data_reference[56..60],
            data_stream,
            &mut logical_block_number,
            block_ranges,
            3,
        )?;
        if self.mediator.debug_output && block_ranges.len() > 0 {
            let last_block_range: &ExtBlockRange = &block_ranges[block_ranges.len() - 1];

            self.mediator
                .debug_print(format!("{:#?}\n\n", last_block_range));
        }
        if logical_block_number < self.number_of_blocks {
            let block_range: ExtBlockRange = ExtBlockRange::new(
                logical_block_number,
                0,
                self.number_of_blocks - logical_block_number,
                ExtBlockRangeType::Sparse,
            );
            if self.mediator.debug_output {
                self.mediator.debug_print(format!("{:#?}\n\n", block_range));
            }
            block_ranges.push(block_range);
        }
        Ok(())
    }

    /// Reads the block numbers tree node from a buffer.
    fn read_node_data(
        &mut self,
        data: &[u8],
        data_stream: &DataStreamReference,
        logical_block_number: &mut u64,
        block_ranges: &mut Vec<ExtBlockRange>,
        depth: u16,
    ) -> io::Result<()> {
        let data_size: usize = data.len();

        let mut number_of_ranges: usize = block_ranges.len();

        for data_offset in (0..data_size).step_by(4) {
            if *logical_block_number >= self.number_of_blocks {
                break;
            }
            let block_number: u32 = bytes_to_u32_le!(data, data_offset);

            if block_number != 0 && depth > 0 {
                let sub_node_offset: u64 = (block_number as u64) * (self.block_size as u64);

                self.read_node_at_position(
                    data_stream,
                    io::SeekFrom::Start(sub_node_offset),
                    logical_block_number,
                    block_ranges,
                    depth,
                )?;
                continue;
            }
            let mut number_of_blocks: u64 = 1;
            if block_number == 0 {
                for _ in 0..depth {
                    number_of_blocks *= self.number_of_blocks_per_block as u64;
                }
                number_of_blocks = min(
                    number_of_blocks,
                    self.number_of_blocks - *logical_block_number,
                );
            }
            let mut create_new_block_range: bool = true;

            if number_of_ranges > 0 {
                let last_block_range: &mut ExtBlockRange = &mut block_ranges[number_of_ranges - 1];

                match last_block_range.range_type {
                    ExtBlockRangeType::InFile => {
                        if (block_number as u64)
                            == last_block_range.physical_block_number
                                + last_block_range.number_of_blocks
                        {
                            last_block_range.number_of_blocks += number_of_blocks;

                            create_new_block_range = false;
                        }
                    }
                    ExtBlockRangeType::Sparse => {
                        if block_number == 0 {
                            last_block_range.number_of_blocks += number_of_blocks;

                            create_new_block_range = false;
                        }
                    }
                }
            };
            if create_new_block_range {
                if self.mediator.debug_output {
                    if number_of_ranges > 0 {
                        let last_block_range: &ExtBlockRange = &block_ranges[number_of_ranges - 1];

                        self.mediator
                            .debug_print(format!("{:#?}\n\n", last_block_range));
                    }
                }
                let range_type: ExtBlockRangeType = if block_number == 0 {
                    ExtBlockRangeType::Sparse
                } else {
                    ExtBlockRangeType::InFile
                };
                let block_range: ExtBlockRange = ExtBlockRange::new(
                    *logical_block_number,
                    block_number as u64,
                    number_of_blocks,
                    range_type,
                );
                block_ranges.push(block_range);

                number_of_ranges += 1;
            }
            *logical_block_number += number_of_blocks;
        }
        Ok(())
    }

    /// Reads the block numbers tree node from a specific position in a data stream.
    fn read_node_at_position(
        &mut self,
        data_stream: &DataStreamReference,
        position: io::SeekFrom,
        logical_block_number: &mut u64,
        block_ranges: &mut Vec<ExtBlockRange>,
        depth: u16,
    ) -> io::Result<()> {
        let mut data: Vec<u8> = vec![0; self.block_size as usize];

        let offset: u64 = match data_stream.write() {
            Ok(mut data_stream) => data_stream.read_exact_at_position(&mut data, position)?,
            Err(error) => return Err(keramics_core::error_to_io_error!(error)),
        };
        if self.mediator.debug_output {
            self.mediator.debug_print(format!(
                "ExtBlockNumbersTreeNode data of size: {} at offset: {} (0x{:08x})\n",
                self.block_size, offset, offset
            ));
            self.mediator.debug_print_data(&data, true);
        }
        self.read_node_data(
            &data,
            data_stream,
            logical_block_number,
            block_ranges,
            depth - 1,
        )
    }
}

