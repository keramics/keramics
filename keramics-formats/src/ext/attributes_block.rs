use std::collections::BTreeMap;

use keramics_core::ErrorTrace;
use keramics_core::mediator::{Mediator, MediatorReference};
use keramics_types::ByteString;

use super::attribute::ExtAttribute;
use super::attributes_entry::ExtAttributesEntry;

/// Extended File System (ext) attributes block.
pub struct ExtAttributesBlock {
    /// Mediator.
    mediator: MediatorReference,
}

impl ExtAttributesBlock {
    /// Creates a new attributes block.
    pub fn new() -> Self {
        Self {
            mediator: Mediator::current(),
        }
    }

    /// Reads the attributes block from a buffer.
    pub fn read_entries(
        &self,
        data: &[u8],
        data_offset: usize,
        data_size: usize,
        entries: &mut Vec<ExtAttribute>,
    ) -> Result<(), ErrorTrace> {
        let mut entry_data_offset: usize = data_offset;

        while entry_data_offset < data_size {
            let mut data_end_offset: usize = entry_data_offset + 8;

            if data_end_offset >= data_size {
                break;
            }
            // The list terminator has the first 4 values set to 0 (8 bytes).
            // Note that some implementations of older Android versions of ext appear to
            // only set the first 4 bytes to 0.
            if data[entry_data_offset..entry_data_offset + 4] == [0; 4] {
                break;
            }
            data_end_offset += 8;

            if data_end_offset >= data_size {
                break;
            }
            let mut entry: ExtAttributesEntry = ExtAttributesEntry::new();

            if self.mediator.debug_output {
                self.mediator
                    .debug_print(ExtAttributesEntry::debug_read_data(
                        &data[entry_data_offset..data_end_offset],
                    ));
            }
            match entry.read_data(&data[entry_data_offset..data_end_offset]) {
                Ok(_) => {}
                Err(mut error) => {
                    keramics_core::error_trace_add_frame!(error, "Unable to read attributes entry");
                    return Err(error);
                }
            }
            entry_data_offset = data_end_offset;

            let attribute: ExtAttribute = ExtAttribute::new();
            // TODO: fill attribute

            let name: ByteString = match entry.read_name(&data[entry_data_offset..]) {
                Ok(name) => name,
                Err(mut error) => {
                    keramics_core::error_trace_add_frame!(error, "Unable to read attribute name");
                    return Err(error);
                }
            };
            if self.mediator.debug_output {
                self.mediator
                    .debug_print(format!("    attribute_name: {},\n", name.to_string()));
            }
            entry_data_offset += entry.name_size as usize;

            let mut alignment_padding_size: usize = entry_data_offset % 4;
            if alignment_padding_size != 0 {
                alignment_padding_size = 4 - alignment_padding_size;
            }
            // TODO: debug print padding data.
            if self.mediator.debug_output {
                self.mediator.debug_print(format!(
                "ExtAttributesBlock: alignment padding data of size: {} at offset: {} (0x{:08x})\n",
                alignment_padding_size,
                entry_data_offset,
                entry_data_offset
            ));
                self.mediator.debug_print_data(
                    &data[entry_data_offset..entry_data_offset + alignment_padding_size],
                    true,
                );
            }

            // TODO: read inline value data.

            entries.push(attribute);

            entry_data_offset += alignment_padding_size;
        }
        Ok(())
    }
}

